public class StringExercise {
    public static void main (String[] args){
        String education = "We don't need no education";
        System.out.println(education +"\n" + education.replace("education", "thought control"));
        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(check);
        String window = "In windows, the main drive is usually C:\\";
        System.out.println(window);
        String slashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(slashes);
    }
}
